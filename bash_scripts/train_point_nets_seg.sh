#!/bin/bash
gpu=0

train () {
  cmd="python3.9 train.py --data fissures --ds ts --pts 2048 --coords --epochs 1 --batch 32 --gpu 0 --model PointNet --output results/PointNet_seg_"$1"_"$2" --kp_mode "$1" --patch "$2" --train_only"
  echo "#######################################################################################################################################################################"
  echo $cmd
  echo "#######################################################################################################################################################################"
  $cmd
}

test () {
  cmd="python3.9 train.py --gpu "$gpu" --output results/PointNet_seg_"$1"_"$2" --test_only"
  echo "#######################################################################################################################################################################"
  echo $cmd
  echo "#######################################################################################################################################################################"
  $cmd
}


train "cnn" "image"
train "enhancement" "image"
train "foerstner" "image"

test "cnn" "image"
test "enhancement" "image"
test "foerstner" "image"
